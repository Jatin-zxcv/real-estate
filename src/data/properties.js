export const getFeaturedProperties = () => properties.filter(p => p.featured);

export const getPropertyById = (id) => properties.find(p => p.id === id);

export const getPropertyBySlug = (slug) => properties.find(p => p.slug === slug);

export const getPropertiesByCategory = (category) => {
  if (category === "ALL") return properties;
  return properties.filter(p => p.category === category);
};

export const formatPrice = (price, isRental = false) => {
  if (isRental) {
    return `₹${price.toLocaleString('en-IN')}/month`;
  }
  if (price >= 10000000) {
    return `₹${(price / 10000000).toFixed(2)} Cr`;
  }
  if (price >= 100000) {
    return `₹${(price / 100000).toFixed(2)} Lakhs`;
  }
  return `₹${price.toLocaleString('en-IN')}`;
};
